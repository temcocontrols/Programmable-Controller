//获得现在是星期几
//功能描述:输入公历日期得到星期(只允许1901-2099年)
//输入参数：公历年月日 
//返回值：星期号																						 
u8 RTC_Get_Week(u16 year, u8 month, u8 day)
{	
	u16 temp2;
	u8 yearH, yearL;
	
	yearH = year / 100;
	yearL = year % 100;
	  
	if(yearH > 19)	// 如果为21世纪,年份数加100
		yearL += 100;
	
	// 所过闰年数只算1900年之后的  
	temp2 = yearL + yearL / 4;
	temp2 = temp2 % 7; 
	temp2 = temp2 + day + table_week[month - 1];
	
	if(yearL % 4 == 0 && month < 3)
		temp2--;
	
	return(temp2 % 7);
}			  
